var ProductBtn = document.querySelectorAll("#add_product_btn");

ProductBtn.forEach((r) => {
    r.addEventListener("click", function (e) {
        let ProductImage = document.getElementById(`image${e.target.value}`);
        let ProductName = document.getElementById(`name${e.target.value}`);
        let ProductPrice = document.getElementById(`price${e.target.value}`);
        localStorage.setItem(`${e.target.value}`, `${ProductImage.src.substring(22, 90)},${ProductName.innerText},${ProductPrice.innerText}`);
    });
});